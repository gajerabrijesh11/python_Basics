# 1. Get the first number from the user
num1_str = input("Enter the first number: ")

# 2. Get the second number from the user
num2_str = input("Enter the second number: ")

# 3. Convert the input strings to integers and add them
sum_of_numbers = int(num1_str) + int(num2_str)

# 4. Display the result to the user
print(f"The sum of {num1_str} and {num2_str} is: {sum_of_numbers}")